                <h4 class="text-uppercase mb-4">Sobre o Projeto</h4>
                <p class="lead mb-0" style="text-align: left; text-align: justify">
                    O objetivo deste projeto foi criar um álbum didático digital sobre Biomas do Sul da Bahia para dar
                    suporte ao ensino dos conteúdos ligados à área da Ecologia. Este álbum didático digital contém
                    imagens de paisagens anteriormente obtidas com as realizações de visitas técnicas, e que contemplem
                    tanto ambientes, como a fauna e flora encontradas. Este projeto pretende oferecer ao aluno certa
                    mobilidade de estudo, na qual ele poderá acessar o álbum em qualquer lugar usando dispositivo com
                    acesso à internet, além de auxiliar nos estudos da disciplina de Biologia. Proporcionando melhoria
                    na qualidade de ensino, além de fornecer uma nova ferramenta para o processo de aprendizado,
                    motivando os alunos a adquirirem novos conhecimentos por meio da utilização do ambiente virtual.


